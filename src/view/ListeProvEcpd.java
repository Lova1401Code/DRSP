    public void fillCombo() throws SQLException{
        String sqlC = "SELECT corps FROM corps";
        AccesBdd bdd = new AccesBdd();
        bdd.loadDriver();
        ResultSet res = bdd.executeSelect(sqlC);
        while (res.next()){
            this.edtCorps.addItem(res.getString("corps"));
        }
    }
    public void fillComboGrade() throws SQLException{
        String sqlGrade = "SELECT grade FROM grade";
        AccesBdd bddGrade = new AccesBdd();
        bddGrade.loadDriver();
        ResultSet resGrade = bddGrade.executeSelect(sqlGrade);
        while (resGrade.next()){
            this.edtGrade.addItem(resGrade.getString("grade"));
        }
    }
