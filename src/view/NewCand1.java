    public void fillCombo() throws SQLException{
        String sqlC = "SELECT corps FROM corps";
        AccesBdd bdd = new AccesBdd();
        bdd.loadDriver();
        ResultSet res = bdd.executeSelect(sqlC);
        while (res.next()){
            this.edtCorps.addItem(res.getString("corps"));
        }
    }
